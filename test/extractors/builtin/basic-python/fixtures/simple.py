"""
Simple utility functions for testing.

@module Utils
@description String and number utilities
"""


def capitalize(text: str) -> str:
    """Capitalize first letter of text.
    
    Args:
        text: Input text
        
    Returns:
        Capitalized text
    """
    return text.capitalize()


def add_numbers(a: int, b: int) -> int:
    """Add two numbers.
    
    Args:
        a: First number
        b: Second number
        
    Returns:
        Sum of a and b
    """
    return a + b
